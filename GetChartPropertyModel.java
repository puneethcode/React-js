package com.verizon.uotm.model;


public class GetChartPropertyModel {
	
	private String gallery;
	private String dataValues;
	private String titles;
	private String legendBox;
	private String view3D;
	private String dataGrid;
	private String allSeries;
	
	
	public String getGallery() {
		return gallery;
	}
	public void setGallery(String gallery) {
		this.gallery = gallery;
	}
	public String getDataValues() {
		return dataValues;
	}
	public void setDataValues(String dataValues) {
		this.dataValues = dataValues;
	}
	public String getTitles() {
		return titles;
	}
	public void setTitles(String titles) {
		this.titles = titles;
	}
	public String getLegendBox() {
		return legendBox;
	}
	public void setLegendBox(String legendBox) {
		this.legendBox = legendBox;
	}
	public String getView3D() {
		return view3D;
	}
	public void setView3D(String view3d) {
		view3D = view3d;
	}
	public String getDataGrid() {
		return dataGrid;
	}
	public void setDataGrid(String dataGrid) {
		this.dataGrid = dataGrid;
	}
	public String getAllSeries() {
		return allSeries;
	}
	public void setAllSeries(String allSeries) {
		this.allSeries = allSeries;
	}
	
}
